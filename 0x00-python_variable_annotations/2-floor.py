#!/usr/bin/env python3
""" 2. Basic annotations - floor """


import math


def floor(n: float) -> int:
    """
    The function `floor` takes a float `n` as argument
    and returns the floor of the float `n` as an int.

    Args:
      n (float): float

    Returns:
      the floor of the float `n` as an int.
    """
    return math.floor(n)
