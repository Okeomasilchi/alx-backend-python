#!/usr/bin/env python3
""" 0. Basic annotations - add """


def add(a: float, b: float) -> float:
    """
    The function `add` takes two float numbers as input
    and returns their sum as a float.

    Args:
      a (float): float
      b (float): The parameters `a` and `b` are both of type
        `float`, and the function `add` takes two float numbers
        `a` and `b` as input and returns their sum as a float.

    Returns:
      the sum of the two input parameters `a` and `b`, which are
      both floats.
    """
    return a + b
